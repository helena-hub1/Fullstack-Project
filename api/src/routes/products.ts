// product router here
import Router from "express";
import passport from "passport";

import {
  createNewProduct,
  getProduct,
  getProducts,
  updateProductInformation,
  deleteProduct,
} from "../controllers/products";

const router = Router();
// routes
router.get("/", getProducts);
router.post(
  "/",
  passport.authenticate("jwt", { session: false }),
  createNewProduct
);
router.get(
  "/:productVIN",
  // passport.authenticate("jwt", { session: false }),
  getProduct
);
router.put(
  "/:productId",
  passport.authenticate("jwt", { session: false }),
  updateProductInformation
);
router.delete(
  "/:productId",
  passport.authenticate("jwt", { session: false }),
  deleteProduct
);

export default router;
