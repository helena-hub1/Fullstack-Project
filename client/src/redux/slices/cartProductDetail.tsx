import { createSlice } from "@reduxjs/toolkit";

import Product from "../../../../common/product";
// type
type InitialState = {
  cartProductDetails: Product;
};
// initial state
const initialState: InitialState = {
  cartProductDetails: {
    VIN: "",
    make: "",
    model: "",
    image: "",
    fueltype: "",
    category: "",
    condtion: "",
    price: 1,
    engine: "",
    color: "",
    year: 1,
    quantity: 1,
    rating: 1,
    description: "",
  },
};
// slice
const cartDetailsSlice = createSlice({
  name: "productDetail",
  initialState,
  reducers: {
    getCartProductDetail: (state, action) => {
      state.cartProductDetails = action.payload;
    },
  },
});
//  actions and reducer
export const cartDetailAction = cartDetailsSlice.actions;
const cartDetailReducer = cartDetailsSlice.reducer;
export default cartDetailReducer;
